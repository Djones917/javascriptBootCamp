let fahrenheit = 50; // 0c  and 273.15k

// calculate celsius and store in celius variable
// print the value
let celsius = (fahrenheit - 32) * 5 / 9;
console.log(celsius);
// calculate kelvin and store in kelvin variable
// print the value
let kelvin = (fahrenheit + 459.67) * 5 / 9;
